    # ─── SVG validation & auto-fix ──────────────────────────────────────

    @staticmethod
    def _validate_and_fix_svg(svg: str) -> Tuple[bool, str]:
        """
        Validate SVG XML and attempt auto-fixes for common LLM output issues.

        LLMs sometimes produce SVG with problems like:
         - Extra text before the <svg> tag (e.g. "Here is the SVG:")
         - Missing xmlns attribute
         - Missing viewBox attribute
         - Malformed XML

        This method tries to fix the first three and then validates with
        Python's XML parser.

        Returns:
            (is_valid, possibly_fixed_svg) — is_valid is True if the SVG
            parses as valid XML after fixes.
        """
        if not svg or not svg.strip():
            return False, svg

        # Fix 1: Strip any text the LLM put before the <svg> opening tag
        svg_start = svg.find("<svg")
        if svg_start > 0:
            svg = svg[svg_start:]
        # Fix 2: Strip any text after the closing </svg> tag
        svg_end = svg.rfind("</svg>")
        if svg_end > 0:
            svg = svg[: svg_end + len("</svg>")]

        # Fix 3: Ensure the xmlns attribute is present (required for valid SVG)
        if 'xmlns="http://www.w3.org/2000/svg"' not in svg:
            svg = svg.replace("<svg", '<svg xmlns="http://www.w3.org/2000/svg"', 1)

        # Ensure viewBox exists
        if "viewBox" not in svg:
            svg = svg.replace(
                "<svg",
                '<svg viewBox="0 0 900 600"',
                1,
            )

        try:
            ET.fromstring(svg.encode("utf-8"))
            return True, svg
        except ET.ParseError as e:
            logger.warning(f"SVG parse error: {e}")
            return False, svg

    # ─── SVG → PNG conversion ────────────────────────────────────────────

    @staticmethod
    def _svg_to_png(svg_content: str) -> bytes:
        """
        Convert an SVG string to PNG image bytes.

        Why two approaches?
        SVG→PNG conversion requires a native graphics library (Cairo).
        Different platforms bundle it differently, so we try two approaches:

        1. svglib + reportlab + pycairo  (works on Windows/Mac/Linux)
           - pycairo bundles the native Cairo DLLs
           - We shim cairocffi → pycairo so reportlab can find it
        2. cairosvg  (works on Linux containers where libcairo is installed)

        If both fail, we return empty bytes (the SVG is still usable).
        """
        if not svg_content:
            return b""

        # ── Approach 1: svglib + reportlab (preferred, cross-platform) ───
        try:
            # The pycairo package bundles native Cairo DLLs.  We register it
            # as "cairocffi" in sys.modules so that reportlab's rlPyCairo
            # backend can find it without a separate cairocffi install.
            import cairo as _pycairo          # pycairo (bundles native DLLs)
            import sys as _sys
            if "cairocffi" not in _sys.modules:
                _sys.modules["cairocffi"] = _pycairo

            import tempfile
            import os
            from svglib.svglib import svg2rlg
            from reportlab.graphics import renderPM

            # svglib needs a file on disk, so write SVG to a temp file
            with tempfile.NamedTemporaryFile(
                suffix=".svg", delete=False, mode="w", encoding="utf-8"
            ) as tmp:
                tmp.write(svg_content)
                tmp_path = tmp.name

            try:
                drawing = svg2rlg(tmp_path)
                if drawing is None:
                    raise ValueError("svglib returned None — SVG may be invalid")
                png_bytes = renderPM.drawToString(drawing, fmt="PNG")
                return png_bytes
            finally:
                os.unlink(tmp_path)

        except Exception as svg_err:
            logger.warning(f"svglib SVG→PNG failed ({svg_err}), trying cairosvg…")

        # ── Approach 2: cairosvg (needs native libcairo — works in Linux) ─
        try:
            import cairosvg
            png_bytes = cairosvg.svg2png(
                bytestring=svg_content.encode("utf-8"),
                output_width=900,
                output_height=600,
            )
            return png_bytes
        except ImportError:
            logger.warning(
                "Neither svglib nor cairosvg could convert SVG→PNG. "
                "Install with: pip install svglib reportlab pycairo"
            )
            return b""
        except Exception as e:
            logger.error(f"SVG→PNG conversion failed: {e}")
            return b""

    # ─── Helpers (used by AI and programmatic code paths) ──────────────

    @staticmethod
    def _strip_code_fences(text: str) -> str:
        """
        Remove markdown code fences from LLM output.

        LLMs often wrap their output in ```xml ... ``` or ```svg ... ```
        even when told not to.  This strips those fences so we get clean SVG.
        """
        text = re.sub(r"^```(?:xml|svg|html)?\s*\n?", "", text, flags=re.MULTILINE)
        text = re.sub(r"\n?```\s*$", "", text, flags=re.MULTILINE)
        return text.strip()

