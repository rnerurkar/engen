@dataclass
class DiagramArtifact:
    """
    Holds all output files for ONE diagram (one DR strategy + one lifecycle phase).

    Example: "Warm Standby / Failover" produces one DiagramArtifact with
    an SVG string, a draw.io XML string, a PNG byte array, and a description.
    """
    dr_strategy: str                  # e.g. "Warm Standby"
    lifecycle_phase: str              # e.g. "Failover"
    svg_content: str = ""             # raw SVG markup (embeddable in HTML)
    drawio_xml: str = ""              # draw.io XML (editable in draw.io app)
    png_bytes: bytes = b""            # raster PNG image bytes
    description: str = ""             # human-readable description of the phase
    is_fallback: bool = False         # True if we had to use a placeholder


@dataclass
class PatternDiagramBundle:
    """
    Container for ALL 12 diagrams of a single pattern.

    The diagrams dict is keyed by (strategy, phase) tuples—so you can look up
    any specific combination like bundle.get("Warm Standby", "Failover").
    """
    pattern_name: str
    diagrams: Dict[Tuple[str, str], DiagramArtifact] = field(default_factory=dict)

    def get(self, strategy: str, phase: str) -> Optional[DiagramArtifact]:
        """Look up a single diagram by strategy + phase.  Returns None if missing."""
        return self.diagrams.get((strategy, phase))

    def all_artifacts(self) -> List[DiagramArtifact]:
        """Return a flat list of every DiagramArtifact in the bundle."""
        return list(self.diagrams.values())


@dataclass
class RegionStates:
    """
    Describes what state each service category should be in for a given
    (DR strategy, lifecycle phase) pair.

    We split services into two categories:
     - "core" = databases & storage (stateful, hard to move)
     - "non-core" = compute & networking (stateless, easy to recreate)

    Each category has a separate state in the Primary and DR regions because
    core and non-core services often behave differently during failover.
    For example, in "Pilot Light On Demand / Initial Provisioning":
     - core services in DR are in Standby (database replica running)
     - non-core services in DR are Not-Deployed (no compute provisioned yet)
    """
    primary_core: str       # State of data/storage services in primary region
    primary_non_core: str   # State of compute/network services in primary region
    dr_core: str            # State of data/storage services in DR region
    dr_non_core: str        # State of compute/network services in DR region
    arrow_label: str        # Label on the arrow between regions (e.g. "Async Replication")


