# ──────────────────────────────────────────────────────────────────────────────
# Service classification: core (stateful) vs. non-core (stateless)
#
# This matters because during a DR event, databases and storage services
# behave very differently from compute services.  A database might be in
# "Standby" (replica running) while compute is "Not-Deployed" (nothing
# provisioned at all).  We use simple keyword matching on the service name
# to decide which category it falls into.
# ──────────────────────────────────────────────────────────────────────────────

# Keywords that identify a service as "core" (stateful data layer).
# If ANY of these substrings appear in the service name, it's core.
# Everything else is classified as "non-core" (compute / network / messaging).
_CORE_SERVICE_KEYWORDS = frozenset({
    "rds", "aurora", "dynamodb", "elasticache", "neptune", "redshift",
    "sql", "spanner", "firestore", "bigtable", "bigquery", "memorystore",
    "s3", "efs", "ebs", "storage", "persistent disk", "filestore",
})


def _is_data_service(service_name: str) -> bool:
    """
    Return True if the service holds persistent state (database / storage).

    Examples:
        _is_data_service("Amazon RDS")  → True   (contains "rds")
        _is_data_service("Amazon EC2")  → False  (no keyword match)
    """
    lower = service_name.lower()
    return any(kw in lower for kw in _CORE_SERVICE_KEYWORDS)

