"""
HA/DR Diagram Generator
=======================
Generates SVG component diagrams and draw.io XML files for every combination
of DR Strategy × Lifecycle Phase (4 strategies × 3 phases = 12 diagrams).

How It Works (high-level flow)
------------------------------
1. The caller passes a list of services (e.g. ["Amazon RDS", "Amazon EC2"]).
2. For each of the 12 (strategy, phase) combos, we look up the expected
   service states in the STATE_MATRIX constant (a big dictionary).
3. Each service is classified as "core" (databases / storage) or
   "non-core" (compute / networking).  Core and non-core services get
   different states depending on the strategy and phase.
4. We build an SVG image and a draw.io XML file that show two regions
   side-by-side (Primary + DR) with every service drawn as a colour-coded
   box matching its state (Active=green, Standby=amber, etc.).
5. The SVG is also converted to PNG for downstream consumers.

Two generation modes
---------------------
- **Programmatic** (default — ``use_ai_diagrams=False``):
  Everything is built from templates in pure Python.  Zero Gemini API calls,
  completes in under 1 second, output is always valid.
- **AI** (opt-in — ``use_ai_diagrams=True``):
  SVG is generated by Gemini for more creative layouts.  draw.io XML is
  still built programmatically because the AI version added no value.

Artefacts produced per diagram
-------------------------------
- SVG  → embeddable in HTML / SharePoint pages
- PNG  → fallback raster image (via svglib or cairosvg)
- draw.io XML → stored on GCS so architects can edit later

Public entry-points
--------------------
- ``generate_all_diagrams()``   — synchronous
- ``agenerate_all_diagrams()``  — async (parallelises AI calls)
"""

