    @staticmethod
    def _split_phases(strategy_text: str) -> Dict[str, str]:
        """
        Split a DR strategy Markdown section into per-phase text blocks.

        The input is a big Markdown string for one DR strategy (e.g. all of
        "Warm Standby").  It contains ### headings for each lifecycle phase.
        We split it into a dict like:
            {"Initial Provisioning": "...", "Failover": "...", "Failback": "..."}

        This is needed because each diagram corresponds to one phase, so we
        need the phase-specific text to pass to the AI prompt or store as
        the artifact description.
        """
        result: Dict[str, str] = {}
        if not strategy_text:
            return result

        # Build a regex that matches ### headings containing any phase name.
        # For example: "### Initial Provisioning" or "### Failover"
        phase_names = "|".join(re.escape(p) for p in LIFECYCLE_PHASES)
        pattern = re.compile(
            rf"(###\s*(?:{phase_names}))", re.IGNORECASE
        )
        # re.split with a capturing group keeps the delimiters in the list.
        # Result: [pre_text, heading1, body1, heading2, body2, ...]
        parts = pattern.split(strategy_text)

        # Walk pairs of (heading, body) starting from index 1
        i = 1
        while i < len(parts) - 1:
            heading = parts[i].strip().lstrip("#").strip()
            body = parts[i + 1].strip()
            # Match the heading text back to the canonical phase name
            for phase in LIFECYCLE_PHASES:
                if phase.lower() in heading.lower():
                    result[phase] = body
                    break
            i += 2

        return result

